def duplicar(numero):
    nx2 = numero * 2
    return nx2

def triplicar(numero):
    nx3 = numero * 3
    return nx3

def quadriplicar(numero):
    nx4 = numero * 4
    return nx4

def multiplicar(numero, multiplicador):
    produto = numero * multiplicador
    return produto

print(duplicar(2))
print(triplicar(2))
print(quadriplicar(2))
print(multiplicar(2, 5))